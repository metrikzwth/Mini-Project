import { useState, useEffect } from "react";
import AdminSidebar from "@/components/layout/AdminSidebar";
import { cn } from "@/lib/utils";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import {
    Table,
    TableBody,
    TableCell,
    TableHead,
    TableHeader,
    TableRow,
} from "@/components/ui/table";
import {
    Dialog,
    DialogContent,
    DialogHeader,
    DialogTitle,
    DialogFooter,
} from "@/components/ui/dialog";
import { Label } from "@/components/ui/label";
import { Trash2, Pencil, Search, Loader2, Wallet, Plus, Minus, Database, HardDrive, AlertCircle } from "lucide-react";
import { toast } from "sonner";
import { getData, setData, STORAGE_KEYS, User, dataChannel } from "@/lib/data";
import { supabase } from "@/lib/supabase";
import { Badge } from "@/components/ui/badge";
import { Alert, AlertDescription, AlertTitle } from "@/components/ui/alert";

interface PatientWithBalance extends User {
    balance?: number;
}

const isUUID = (id: string) => /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(id);

const AdminPatients = () => {
    const [patients, setPatients] = useState<PatientWithBalance[]>([]);
    const [search, setSearch] = useState("");
    const [loading, setLoading] = useState(true);
    const [editingUser, setEditingUser] = useState<PatientWithBalance | null>(null);
    const [editForm, setEditForm] = useState({ name: "", email: "", phone: "", address: "" });
    const [isEditOpen, setIsEditOpen] = useState(false);

    // Wallet Adjustment State
    const [walletAmount, setWalletAmount] = useState("");
    const [walletAction, setWalletAction] = useState<'add' | 'deduct'>('add');
    const [processingWallet, setProcessingWallet] = useState(false);

    useEffect(() => {
        loadPatients();

        // Listen for cross-tab updates
        const channel = new BroadcastChannel('medicare_data_updates');
        channel.onmessage = (event) => {
            if (event.data.type === 'update') {
                loadPatients();
            }
        };

        // Listen for local updates
        const handleLocalUpdate = () => {
            loadPatients();
        };
        window.addEventListener('localDataUpdate', handleLocalUpdate);

        return () => {
            channel.close();
            window.removeEventListener('localDataUpdate', handleLocalUpdate);
        };
    }, []);

    const loadPatients = async () => {
        setLoading(true);
        try {
            // 1. Load from Local Storage Mock first (as baseline)
            const localUsers = getData<User[]>(STORAGE_KEYS.USERS, []);
            let allPatients: PatientWithBalance[] = localUsers.filter(u => u.role === 'patient');

            // 2. Fetch Real Profiles from Supabase
            try {
                const { data: dbProfiles, error: profileError } = await supabase
                    .from('profiles')
                    .select('*')
                    .eq('role', 'patient');

                if (dbProfiles && !profileError) {
                    // Map DB profiles to User/PatientWithBalance structure
                    const dbPatients: PatientWithBalance[] = dbProfiles.map(p => ({
                        id: p.id,
                        email: p.email,
                        name: p.full_name || 'Unknown',
                        role: 'patient',
                        phone: p.phone,
                        address: p.address,
                        password: '', // Not needed for admin view
                        balance: 0 // Default
                    }));

                    // Merge: Use DB patients as primary. 
                    // Deduplicate by ID AND Email. 
                    // If a user is in DB, we ignore the local storage version with the same email.

                    const dbPatientMap = new Map(dbPatients.map(p => [p.id, p]));
                    const dbEmailSet = new Set(dbPatients.map(p => p.email.toLowerCase()));

                    const mergedPatients: PatientWithBalance[] = [...dbPatients];

                    localUsers.forEach(localUser => {
                        // Only add local user if checking both ID and Email shows it's new
                        if (localUser.role === 'patient' &&
                            !dbPatientMap.has(localUser.id) &&
                            !dbEmailSet.has(localUser.email.toLowerCase())) {
                            mergedPatients.push(localUser);
                        }
                    });

                    allPatients = mergedPatients;
                }
            } catch (err) {
                console.log("Profile sync failed", err);
            }

            // 3. Fetch Wallet Balances from Supabase with timeout
            try {
                const timeoutPromise = new Promise((_, reject) =>
                    setTimeout(() => reject(new Error('Timeout')), 5000)
                );

                const dbPromise = supabase.from('wallets').select('user_id, balance');

                // Race condition: if DB is too slow, we just show mapped data with 0 balance
                const result = await Promise.race([dbPromise, timeoutPromise]) as any;

                if (result && result.data) {
                    const wallets = result.data;
                    allPatients = allPatients.map(p => ({
                        ...p,
                        balance: wallets.find((w: any) => w.user_id === p.id)?.balance || 0
                    }));
                }
            } catch (err) {
                console.log("Wallet sync failed or timed out", err);
            }

            setPatients(allPatients);
        } catch (e) {
            console.error("Critical error loading patients", e);
            toast.error("Failed to load patients");
        } finally {
            setLoading(false);
        }
    };

    const handleDelete = async (userId: string) => {
        if (!confirm("Are you sure you want to terminate this patient's account?")) return;

        const allUsers = getData<User[]>(STORAGE_KEYS.USERS, []);
        const updatedUsers = allUsers.filter(u => u.id !== userId);
        setData(STORAGE_KEYS.USERS, updatedUsers);

        // Delete from DB if valid UUID
        if (isUUID(userId)) {
            try {
                await supabase.from('profiles').delete().eq('id', userId);
            } catch (e) {
                console.error(e);
            }
        }

        toast.success("Patient account terminated");
        loadPatients();
    };

    const handleEdit = (user: PatientWithBalance) => {
        setEditingUser(user);
        setEditForm({
            name: user.name,
            email: user.email,
            phone: user.phone || "",
            address: user.address || ""
        });
        setWalletAmount(""); // Reset wallet input
        setIsEditOpen(true);
    };

    const saveEdit = async () => {
        if (!editingUser) return;

        // 1. Update Mock (Always works for local state)
        const allUsers = getData<User[]>(STORAGE_KEYS.USERS, []);
        const updatedUsers = allUsers.map(u => {
            if (u.id === editingUser.id) {
                return { ...u, name: editForm.name, email: editForm.email, phone: editForm.phone, address: editForm.address };
            }
            return u;
        });
        setData(STORAGE_KEYS.USERS, updatedUsers);

        // 2. Update DB (Only if valid UUID)
        if (isUUID(editingUser.id)) {
            try {
                const { error } = await supabase.from('profiles').update({
                    full_name: editForm.name,
                    email: editForm.email,
                    phone: editForm.phone,
                    address: editForm.address
                }).eq('id', editingUser.id);

                if (error) throw error;
            } catch (e: any) {
                console.error("DB update error", e);
                toast.error(`Failed to sync with DB: ${e.message}`);
                // Don't return, we still updated local mock
            }
        } else {
            console.log("Skipping DB update for Local user:", editingUser.id);
        }

        toast.success("Patient details updated");
        setIsEditOpen(false);
        loadPatients();
    };

    const handleWalletUpdate = async () => {
        if (!editingUser || !walletAmount) return;
        const amount = parseFloat(walletAmount);
        if (isNaN(amount) || amount <= 0) {
            toast.error("Invalid amount");
            return;
        }

        setProcessingWallet(true);
        try {
            // Handle Local User (Mock)
            if (!isUUID(editingUser.id)) {
                const allUsers = getData<any[]>(STORAGE_KEYS.USERS, []);
                const updatedUsers = allUsers.map(u => {
                    if (u.id === editingUser.id) {
                        const currentBalance = u.balance || 0;
                        const newBalance = walletAction === 'add'
                            ? currentBalance + amount
                            : Math.max(0, currentBalance - amount);
                        return { ...u, balance: newBalance };
                    }
                    return u;
                });
                setData(STORAGE_KEYS.USERS, updatedUsers);

                toast.success("Wallet balance updated (Local)");
                setWalletAmount("");
                setEditingUser(prev => prev ? ({
                    ...prev,
                    balance: walletAction === 'add'
                        ? (prev.balance || 0) + amount
                        : Math.max(0, (prev.balance || 0) - amount)
                }) : null);
                await loadPatients();
                return;
            }

            // Handle Real DB User
            const description = `Admin Adjustment: ${walletAction === 'add' ? 'Credit' : 'Debit'} via Patient Management`;
            const rpcName = walletAction === 'add' ? 'add_credits' : 'deduct_credits';

            const params: any = {
                target_user_id: editingUser.id,
                txn_description: description
            };

            if (walletAction === 'add') {
                params.credit_amount = amount;
            } else {
                params.deduct_amount = amount;
                params.txn_type = 'manual_adjustment';
            }

            // check allow RPC to process
            const { error } = await supabase.rpc(rpcName, params);

            if (error) {
                throw error;
            } else {
                toast.success("Wallet balance updated successfully");
                setWalletAmount("");
                // Update the local state for immediate feedback
                setEditingUser(prev => prev ? ({
                    ...prev,
                    balance: walletAction === 'add'
                        ? (prev.balance || 0) + amount
                        : Math.max(0, (prev.balance || 0) - amount)
                }) : null);

                // Force broadcast update for other tabs
                dataChannel.postMessage({ type: 'update', key: 'wallet_db_update' });

                // Reload to sync everything
                await loadPatients();
            }
        } catch (error: any) {
            console.error("Wallet update error:", error);
            toast.error(`Failed to update wallet: ${error.message || "Unknown error"}`);
        } finally {
            setProcessingWallet(false);
        }
    };

    const filteredPatients = patients.filter(p =>
        p.name.toLowerCase().includes(search.toLowerCase()) ||
        p.email.toLowerCase().includes(search.toLowerCase())
    );

    return (
        <div className="min-h-screen bg-background m-5">
            <AdminSidebar />
            <main className={cn("transition-all pt-16 lg:pt-0 lg:pl-64", "p-8")}>
                <div className="flex justify-between items-center mb-8">
                    <h1 className="text-3xl font-bold text-foreground">Patients Management</h1>
                    <div className="relative w-72">
                        <Search className="absolute left-2 top-2.5 h-4 w-4 text-muted-foreground" />
                        <Input
                            placeholder="Search patients..."
                            className="pl-8"
                            value={search}
                            onChange={(e) => setSearch(e.target.value)}
                        />
                    </div>
                </div>

                <div className="rounded-md border bg-card">
                    <Table>
                        <TableHeader>
                            <TableRow>
                                <TableHead>Name</TableHead>
                                <TableHead>Email</TableHead>
                                <TableHead>Status</TableHead>
                                <TableHead>Phone</TableHead>
                                <TableHead>Balance</TableHead>
                                <TableHead className="text-right">Actions</TableHead>
                            </TableRow>
                        </TableHeader>
                        <TableBody>
                            {loading ? (
                                <TableRow>
                                    <TableCell colSpan={6} className="text-center py-8">
                                        <Loader2 className="w-6 h-6 animate-spin mx-auto" />
                                    </TableCell>
                                </TableRow>
                            ) : filteredPatients.length === 0 ? (
                                <TableRow>
                                    <TableCell colSpan={6} className="text-center py-8 text-muted-foreground">
                                        No patients found
                                    </TableCell>
                                </TableRow>
                            ) : (
                                filteredPatients.map((patient) => (
                                    <TableRow key={patient.id}>
                                        <TableCell>
                                            <div className="flex items-center gap-3">
                                                <div className="w-10 h-10 rounded-full bg-secondary/10 flex items-center justify-center overflow-hidden border">
                                                    {patient.image ? (
                                                        <img src={patient.image} alt={patient.name} className="w-full h-full object-cover" />
                                                    ) : (
                                                        <span className="text-xs font-bold text-secondary">{patient.name.charAt(0)}</span>
                                                    )}
                                                </div>
                                                <span className="font-medium">{patient.name}</span>
                                            </div>
                                        </TableCell>
                                        <TableCell>{patient.email}</TableCell>
                                        <TableCell>
                                            {isUUID(patient.id) ? (
                                                <Badge variant="outline" className="bg-green-50 text-green-700 border-green-200 gap-1">
                                                    <Database className="w-3 h-3" /> Synced
                                                </Badge>
                                            ) : (
                                                <Badge variant="outline" className="bg-orange-50 text-orange-700 border-orange-200 gap-1">
                                                    <HardDrive className="w-3 h-3" /> Local
                                                </Badge>
                                            )}
                                        </TableCell>
                                        <TableCell>{patient.phone || "-"}</TableCell>
                                        <TableCell className="font-mono">
                                            {isUUID(patient.id) ? `$${patient.balance?.toFixed(2) || '0.00'}` : <span className="text-muted-foreground text-xs">N/A</span>}
                                        </TableCell>
                                        <TableCell className="text-right space-x-2">
                                            <Button variant="ghost" size="icon" onClick={() => handleEdit(patient)}>
                                                <Pencil className="w-4 h-4" />
                                            </Button>
                                            <Button variant="ghost" size="icon" className="text-red-500 hover:text-red-700 hover:bg-red-50" onClick={() => handleDelete(patient.id)}>
                                                <Trash2 className="w-4 h-4" />
                                            </Button>
                                        </TableCell>
                                    </TableRow>
                                ))
                            )}
                        </TableBody>
                    </Table>
                </div>

                <Dialog open={isEditOpen} onOpenChange={setIsEditOpen}>
                    <DialogContent className="max-w-lg">
                        <DialogHeader>
                            <DialogTitle>Edit Patient Details</DialogTitle>
                        </DialogHeader>
                        <div className="space-y-6 py-4">
                            {/* Personal Info Section */}
                            <div className="space-y-4">
                                <h3 className="text-sm font-medium text-muted-foreground uppercase tracking-wider">Personal Information</h3>
                                <div className="grid grid-cols-2 gap-4">
                                    <div className="space-y-2">
                                        <Label>Full Name</Label>
                                        <Input value={editForm.name} onChange={e => setEditForm({ ...editForm, name: e.target.value })} />
                                    </div>
                                    <div className="space-y-2">
                                        <Label>Email</Label>
                                        <Input value={editForm.email} onChange={e => setEditForm({ ...editForm, email: e.target.value })} type="email" />
                                    </div>
                                    <div className="space-y-2">
                                        <Label>Phone</Label>
                                        <Input value={editForm.phone} onChange={e => setEditForm({ ...editForm, phone: e.target.value })} />
                                    </div>
                                    <div className="space-y-2">
                                        <Label>Address</Label>
                                        <Input value={editForm.address} onChange={e => setEditForm({ ...editForm, address: e.target.value })} />
                                    </div>
                                </div>
                            </div>

                            {/* Wallet Section */}
                            <div className="space-y-4 pt-4 border-t">
                                <h3 className="text-sm font-medium text-muted-foreground uppercase tracking-wider flex items-center gap-2">
                                    <Wallet className="w-4 h-4" /> Wallet Management
                                </h3>

                                <>
                                    <div className="bg-muted/50 p-4 rounded-lg flex items-center justify-between">
                                        <span className="text-sm font-medium">Current Balance</span>
                                        <span className="text-2xl font-bold font-mono">
                                            ${editingUser?.balance?.toFixed(2) || '0.00'}
                                        </span>
                                    </div>
                                    <div className="flex items-end gap-3">
                                        <div className="space-y-2 flex-1">
                                            <Label>Adjust Balance</Label>
                                            <div className="flex items-center gap-2">
                                                <div className="flex border rounded-md overflow-hidden shrink-0">
                                                    <button
                                                        className={cn("px-3 py-2 hover:bg-muted transition-colors", walletAction === 'add' ? 'bg-primary text-primary-foreground hover:bg-primary/90' : 'bg-background')}
                                                        onClick={() => setWalletAction('add')}
                                                        title="Add Credits"
                                                    >
                                                        <Plus className="w-4 h-4" />
                                                    </button>
                                                    <button
                                                        className={cn("px-3 py-2 hover:bg-muted transition-colors", walletAction === 'deduct' ? 'bg-destructive text-destructive-foreground hover:bg-destructive/90' : 'bg-background')}
                                                        onClick={() => setWalletAction('deduct')}
                                                        title="Deduct Credits"
                                                    >
                                                        <Minus className="w-4 h-4" />
                                                    </button>
                                                </div>
                                                <Input
                                                    type="number"
                                                    placeholder="Amount"
                                                    value={walletAmount}
                                                    onChange={e => setWalletAmount(e.target.value)}
                                                />
                                            </div>
                                        </div>
                                        <Button onClick={handleWalletUpdate} disabled={processingWallet} variant="secondary">
                                            {processingWallet ? "Processing..." : "Update Wallet"}
                                        </Button>
                                    </div>
                                </>
                            </div>
                        </div>
                        <DialogFooter>
                            <Button onClick={saveEdit}>Save Changes</Button>
                        </DialogFooter>
                    </DialogContent>
                </Dialog>
            </main>
        </div>
    );
};

export default AdminPatients;
